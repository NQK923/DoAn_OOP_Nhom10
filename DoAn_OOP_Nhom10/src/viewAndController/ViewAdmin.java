    private void checkIfCreateAcc() {
        String GMAIL_REGEX = "^[\\w-_\\.+]*[\\w-_\\.]\\@([\\w]+\\.)+[\\w]+[\\w]$";
        boolean checkGmail = (gmailCreate.getText()).matches(GMAIL_REGEX);
        if (gmailCreate.getText().equals("") || nameCreate.getText().equals("") || PassCreate.getPassword().equals("") || ConfirmCreate.getPassword().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Chưa nhập đủ thông tin!");
            return;
        }
        if (checkGmail == false) {
            JOptionPane.showMessageDialog(rootPane, "Gmail không hợp lệ!");
            return;
        }
        if ((nameCreate.getText()).length() < 6) {
            JOptionPane.showMessageDialog(rootPane, "Tên đăng nhập phải >=6 kí tự!");
            return;
        }
        if ((PassCreate.getPassword()).length < 8) {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu phải >=8 kí tự!");
            return;
        }
        if (!String.valueOf(PassCreate.getPassword()).equals(String.valueOf(ConfirmCreate.getPassword()))) {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu xác thực không chính xác!");
            return;
        }
        List<account> listAcc = new ArrayList<>();
        listAcc = ConnectToXampp.findAllAccount();
        for (account acc : listAcc) {
            if (acc.getGmail().equals(gmailCreate.getText()) || acc.getName().equals(nameCreate.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Gmail hoặc tên đăng nhập đã tồn tại!");
                return;
            }
        }
        account acc = new account(gmailCreate.getText(), nameCreate.getText(), String.valueOf(PassCreate.getPassword()));
        ConnectToXampp.insertAccount(acc);
        gmailCreate.setText("");
        nameCreate.setText("");
        PassCreate.setText("");
        ConfirmCreate.setText("");
        JOptionPane.showMessageDialog(rootPane, "Tạo tài khoản thành công!");
    }

