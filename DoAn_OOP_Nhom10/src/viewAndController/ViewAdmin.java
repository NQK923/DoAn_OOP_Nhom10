    private int checkCinInput() {
        if (EditCinCode.getText().equals("") || EditCode.getText().equals("") || EditName.getText().equals("") || EditColor.getText().equals("")
                || EditAmount.getText().equals("") || EditSubflower.getText().equals("") || EditPetalsFlower.getText().equals("") || typeFlowerFormEdit.equals("")) {
            return 1;
        } else {
            for (listFlower flower : list) {
                if (EditCinCode.getText().equals(flower.getCode()) && EditCinCode.getText().equals(EditCode.getText())) {
                    listFlower FL = new listFlower(EditName.getText(), EditCode.getText(), typeFlowerFormEdit, EditColor.getText(), Integer.parseInt(EditAmount.getText()), Integer.parseInt(EditPetalsFlower.getText()), Integer.parseInt(EditSubflower.getText()), flower.getDay(), flower.getMonth(), flower.getYear());
                    ConnectToXampp.update(FL);
                    ConnectToXampp.insertEdit(FL);
                    getDataFromDataBase();
                    return 2;
                }
            }
        }
        return 0;
    }
