    private void checkTime() {
        Calendar Cal = Calendar.getInstance();
        toY = Cal.get(Calendar.YEAR);
        toM = Cal.get(Calendar.MONTH) + 1;
        toD = Cal.get(Calendar.DAY_OF_MONTH);
        fromY = 1;
        fromM = 1;
        fromD = 1;

        if ((!fromDay.getText().equals("dd") && !fromDay.getText().equals(""))) {
            fromD = Integer.parseInt(fromDay.getText());
        }
        if ((!fromMonth.getText().equals("mm") && !fromMonth.getText().equals(""))) {
            fromM = Integer.parseInt(fromMonth.getText());
        }
        if ((!fromYear.getText().equals("yyyy") && !fromYear.getText().equals(""))) {
            fromY = Integer.parseInt(fromYear.getText());
        }
        if ((!toDay.getText().equals("dd") && !toDay.getText().equals(""))) {
            toD = Integer.parseInt(toDay.getText());
        }
        if ((!toMonth.getText().equals("mm") && !toMonth.getText().equals(""))) {
            toM = Integer.parseInt(toMonth.getText());
        }
        if ((!toYear.getText().equals("yyyy") && !toYear.getText().equals(""))) {
            toY = Integer.parseInt(toYear.getText());
        }
    }
    private int compareTime(LocalDate timeF, LocalDate timeH, LocalDate timeT){
        if(timeF.compareTo(timeT) <= 0){
            if(timeF.compareTo(timeH) <= 0 && timeH.compareTo(timeT) <= 0){
                return 1;
            }
        }else{
            return 2;
        }
        return 0;
    }
    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        List<listFlower> listSearchTmp = new ArrayList<>();
        String input = (inputSearch.getText()).toLowerCase();
        checkTime();
        LocalDate timeF = LocalDate.of(fromY, fromM, fromD);
        LocalDate timeT = LocalDate.of(toY, toM, toD);
        if(compareTime(timeF, timeF, timeT) == 2){
            JOptionPane.showMessageDialog(rootPane, "dd/mm/yyyy-start > dd/mm/yyyy-end  ???");
        }
        for (listFlower flower : list) {
            String code = flower.getCode().toLowerCase();
            String name = flower.getName().toLowerCase();
            String color = flower.getColor().toLowerCase();
            LocalDate timeH = LocalDate.of(flower.getYear(), flower.getMonth(), flower.getDay());
            System.err.println(timeH);
            if (code.indexOf(input) >= 0 || name.indexOf(input) >= 0 || color.indexOf(input) >= 0) {
                if (!typeFlowerSearch.equals("")) {
                    if (flower.getType().equals(typeFlowerSearch) && compareTime(timeF, timeH, timeT) == 1) {
                        listSearchTmp.add(flower);
                    }
                }else{
                    if(compareTime(timeF, timeH, timeT) == 1){
                        listSearchTmp.add(flower);
                    }
                }
            }
        }
        renderTableSearch(listSearchTmp);
    }//GEN-LAST:event_btnSearchActionPerformed

